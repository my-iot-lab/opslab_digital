namespace Admin.NET.Core.Service
{
    [NotTable]
    public class TimerOutput : SysTimer
    {
        /// <summary>
        /// 执行次数
        /// </summary>
        public long Tally { get; set; }

        /// <summary>
        /// 任务状态
        /// </summary>
        public SpareTimeStatus Status { get; set; } = SpareTimeStatus.Stopped;

        /// <summary>
        /// 异常信息
        /// </summary>
        public string Exception { get; set; }
    }
}